package sv.edu.usam.operaciones;
public class Operar {
    public double suma(double val1, double val2) {
        return (val1 + val2);
    }
    public double resta(double val1, double val2) {
        return (val1 - val2);
    }
    public double multi(double val1, double val2) {
        return (val1 * val2);
    }
    public double div(double val1, double val2) {
        return (val1 / val2);
    }
}
